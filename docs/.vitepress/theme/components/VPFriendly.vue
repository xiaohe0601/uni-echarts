<template>
  <div v-if="list.length > 0" class="VPFeatures">
    <div class="container">
      <h1 class="title">友情链接</h1>

      <div class="items">
        <div
          v-for="(item) in list"
          :key="item.id"
          class="item"
          :class="itemClasses">
          <VPFeature
            :icon="item.icon"
            :title="item.title"
            :details="item.details"
            :link="item.link"></VPFeature>
        </div>
      </div>
    </div>
  </div>
</template>

<script lang="ts" setup>
import VPFeature from "vitepress/dist/client/theme-default/components/VPFeature.vue";
import { computed, reactive } from "vue";
import { useFriendly } from "../composables/useFriendly";

const friendly = reactive(useFriendly());

const list = computed(() => {
  if (friendly.data == null) {
    return [];
  }

  return friendly.data.map((item) => ({
    ...item,
    icon: {
      src: item.icon
    }
  }));
});

const itemClasses = computed(() => {
  const value: any[] = [];

  const length = list.value.length;

  if (length === 2) {
    return "grid-2";
  } else if (length === 3) {
    return "grid-3";
  } else if (length % 3 === 0) {
    return "grid-6";
  } else if (length > 3) {
    return "grid-4";
  }

  return value;
});
</script>

